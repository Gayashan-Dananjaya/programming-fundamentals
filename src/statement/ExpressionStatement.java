package statement;

import java.util.Scanner;

public class ExpressionStatement {
    public static void main(String[] args) {

//        assignment expressions
        int x = 10;
        x = 10 + 20;

//        postfix increment
        x++;

//        postfix decrement
        x--;

//        prefix increment
        ++x;

//        prefix decrement
        --x;

//        method invocation expression statement
        System.out.println("IJSE");

//        New instace creatin expression
        new Scanner(System.in);



    }
}
